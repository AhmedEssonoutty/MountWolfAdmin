// copy text
const copied = ref(false);
import * as ClipboardJS from "clipboard-polyfill";
import { storeToRefs } from "pinia";

const copyText = (text, e) => {
  let className = e.currentTarget.classList[1];
  let svg = document.querySelector(`svg.${className}`);
  let feather = document.querySelector(
    `svg.icons-${className.charAt(className.length - 1)}`
  );
  svg.classList.toggle("show");
  svg.classList.toggle("hide");
  feather.classList.toggle("hide");
  feather.classList.toggle("show");

  setTimeout(() => {
    svg.classList.toggle("show");
    svg.classList.toggle("hide");
    feather.classList.toggle("hide");
    feather.classList.toggle("show");
  }, 500);

  if (text) {
    copied.value = true;
    ClipboardJS.writeText(text).catch((err) => {
      console.error("Unable to copy text", err);
    });
  }
  setTimeout(() => {
    copied.value = false;
  }, 1000);
};
